public class CharDegiskenler {
    public static void main(String[] args) {
        char karakter = 'A';
        System.out.println("Karakter: " + karakter);
        karakter = 'A' + 1;
        System.out.println("Karakter: " + karakter);
        karakter = 'a';
        int ascii = (int) karakter;
        System.out.println("Karakter: " + karakter);
        System.out.println("ASCII kodu: " + ascii);
    }
}
